pub fn color_to_hex(color: Color) -> String {
    match color {
        Color::Rgb(r, g, b) => format!("#{:02x}{:02x}{:02x}", r, g, b),
        Color::Black => "#000000".to_string(),
        Color::DarkGray => "#545454".to_string(),
        Color::Gray => "#a8a8a8".to_string(),
        Color::White => "#ffffff".to_string(),
        Color::Indexed(idx) => match idx {
            0 => "#000000".to_string(),
            1 => "#aa0000".to_string(),
            2 => "#00aa00".to_string(),
            3 => "#aa5500".to_string(),
            4 => "#0000aa".to_string(),
            5 => "#aa00aa".to_string(),
            6 => "#00aaaa".to_string(),
            7 => "#aaaaaa".to_string(),
            8 => "#555555".to_string(),
            9 => "#ff5555".to_string(),
            10 => "#55ff55".to_string(),
            11 => "#ffff55".to_string(),
            12 => "#5555ff".to_string(),
            13 => "#ff55ff".to_string(),
            14 => "#55ffff".to_string(),
            15 => "#ffffff".to_string(),
            _ => "#000000".to_string(),
        },
        _ => "#000000".to_string(),
    }
}

